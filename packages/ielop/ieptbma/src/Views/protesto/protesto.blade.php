<h2 class="titulo">Protesto</h2>
<p>O protesto é o ato formal e solene pelo qual se prova a inadimplência de uma determinada pessoa, física ou jurídica, pública ou privada, quando esta é devedora de um título de crédito ou de outro documento de dívida. Dessa forma, pelo protesto se prova publicamente o atraso do devedor no pagamento na dívida, protegendo os direitos do credor.</p>
<p>O protesto somente é lavrado e registrado pelo Tabelião de protestos – ou seu preposto devidamente designado –, que é a pessoa investida nesse cargo em virtude de delegação do Poder Público, nos termos da lei. Tudo em consonância com a Constituição da República Federativa do Brasil de 1988 e o Estado Democrático de Direito.</p>
<ul>
    <li class="box">
        <strong class="subtitulo">Prescrição</strong>
        <p>O registro do protesto não prescreve; o que prescreve é o título. Portanto, após o prazo legal, o título deixa de ter restrição para consulta pública em cadastros de inadimplentes, mas o protesto lavrado no Cartório continua preservado, indefinidamente.</p>
    </li>
    <li class="box">
        <strong class="subtitulo">Prova Inadimplência</strong>
        <p>Por se tratar de ato formal e solene, força o devedor ao pagamento da dívida em atraso. Pois prova a inadimplência, ou seja, o descumprimento de obrigação originada em títulos e outros documentos de dívida, bem como estabelece prazo de vencimento quando não há data preestabelecida. Facilita, assim, o recebimento da dívida pelo credor.</p>
    </li>
    <li class="box">
        <strong class="subtitulo">SERASA, SPC</strong>
        <p>A inclusão nos serviços de proteção ao crédito pelos cartórios ocorre somente após um procedimento legal, com ampla publicidade e obediência à lei, resguardando, por exemplo, quanto à notificação obrigatória do devedor prevista do CDC. Sempre fiscalizado pelo Poder Judiciário.</p>
    </li>
    <li class="box">
        <strong class="subtitulo">Prévia Intimação</strong>
        <p>Momento em que lhe é dada oportunidade de pagar o débito devido. O que condiciona um elevado número de pagamentos dos títulos encaminhados para o cartório antes mesmo do protesto.</p>
    </li>
    <li class="box">
        <strong class="subtitulo">DISPONIBILIZA A CENTRAL DE REMESSA DE ARQUIVOS (CRA)</strong>
	<p>Os Cartórios de Protesto e a Central de Remessas de Arquivos do Maranhão (CRA-MA) permanecem em constantes trocas de informações digitais, ou seja, os arquivos virtuais de títulos são recepcionados e enviados a partir da plataforma virtual da CRA-MA, onde é possível verificar o andamento dos títulos e outras informações suplementares, estreitando, assim, o relacionamento dos Apresentes/Cartórios, aliado à já conhecida eficiência dos serviço prestados pelas Serventias Extrajudiciais.</p>
    </li>
</ul>
